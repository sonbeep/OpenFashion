package com.example.openfashion.model;

public class Infor {
    private int avatar;
    private String name;

    public Infor(int avatar, String name) {
        this.avatar = avatar;
        this.name = name;
    }

    public int getAvatar() {
        return avatar;
    }

    public String getName() {
        return name;
    }
}
